package variable;

public class Var8 {
    public static void main(String[] args) {
        // 정수
        byte b = 127; // -128 ~ 127 (2^8)
        short s = 32767; // -32,768 ~ 32,767 (2^16)
        int i = 2147483647; // -2,147,483,648 ~ 2,147,483,647 (약 20억) (2^16)
        long l = 9223372036854775807L; // -9,223,372,036,854,775,808 ~ 9,223,372,036,854,775,807 (2^32)

        // 실수
        float f = 10.0f; // 7자리 정밀도 (2^32)
        double d = 10.0; // 15자리 정밀도 (2^64)
    }
}
